    private void jTextField2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyReleased
        // TODO add your handling code here:
        String exitemail = jTextField2.getText();
        Validations v1= new Validations();
        if(exitemail.isEmpty()){
            jLabel6.setVisible(false);
        }
        else{
        jLabel6.setVisible(true);
        if(v1.emailValidate(exitemail)){
            jLabel6.setIcon(new javax.swing.ImageIcon(getClass().getResource("/image/a.png")));
        }
        else{
            jLabel6.setIcon(new javax.swing.ImageIcon(getClass().getResource("/image/b.png")));  
        }
        }
        
        try(
                Connection con=DBConnection.getConnect();
                PreparedStatement ps = con.prepareStatement("Select * from register where email='"+exitemail+"'");
                ResultSet rs= ps.executeQuery();
                )
        {
            if(rs.next()){
                jLabel5.setVisible(true);
                jLabel5.setText("Email exist in our Database !!");
                jLabel6.setIcon(new javax.swing.ImageIcon(getClass().getResource("/image/b.png")));
                b = true;
            }
            
            else{
                jLabel5.setVisible(false);
                b = false;
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTextField2KeyReleased

