    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name1 = jTextField1.getText();
        name1 = name1.substring(0,1).toUpperCase() + name1.substring(1).toLowerCase();
        String email1 = jTextField2.getText();
        char[] pass = jPasswordField1.getPassword();
        String pass1= new String(pass);
        String phoneno1 = jTextField3.getText();
        String module = "employee";
        
        Validations v = new Validations();
        if(!v.emailValidate(email1)){
            if(email1.isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Email is empty !!", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else{
            JOptionPane.showMessageDialog(rootPane, "Please Enter Valid Email ID !!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        }
        
        
        
        else if(b){
            JOptionPane.showMessageDialog(rootPane, "Enter another Valid Email !!");
            jLabel5.setVisible(false);
            jTextField1.setText("");
            jTextField2.setText("");
            jPasswordField1.setText("");
            jTextField3.setText("");
        }
        else{
        try(
                Connection con=DBConnection.getConnect();
                PreparedStatement ps = con.prepareStatement("insert into register values(?,?,?,?,?)");
                )
        {
            ps.setString(1, name1);
            ps.setString(2, email1);
            ps.setString(3, pass1);
            ps.setString(4, phoneno1);
            ps.setString(5, module);
            
            int i = ps.executeUpdate();
            if(i>0){
                JOptionPane.showMessageDialog(rootPane, "Sucessfully Added.");
                jTextField1.setText("");
                jTextField2.setText("");
                jPasswordField1.setText("");
                jTextField3.setText("");
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Didn't support due to some error");
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

