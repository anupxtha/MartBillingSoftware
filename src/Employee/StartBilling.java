    private void jTextField6KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField6KeyReleased
        // TODO add your handling code here:
        String itemid = jTextField6.getText();
        String itemName, itemPrice, itemCategory,itemDesc;
        try(
                Connection con=DBConnection.getConnect();
                PreparedStatement ps = con.prepareStatement("Select * from items where item_id='"+itemid+"'");
                ResultSet rs = ps.executeQuery();
                )
        {
            if(rs.next()){
                itemName = rs.getString("item_name");
                itemPrice = rs.getString("item_price");
                itemCategory = rs.getString("item_category");
                itemDesc = rs.getString("item_desc");
                //itemQuantity = rs.getString("item_quantity");
                BufferedImage im = ImageIO.read(rs.getBinaryStream("item_image"));
                jLabel9.setIcon(new ImageIcon(im.getScaledInstance(jLabel9.getWidth(), jLabel9.getHeight(), Image.SCALE_DEFAULT)));
                
                jTextField1.setText(itemName);
                jTextField2.setText(itemPrice);
                jTextField3.setText(itemDesc);
                jTextField4.setText(itemCategory);
                
            }
            else{
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jComboBox1.setSelectedItem("Select Quantity");
                jTextField5.setText("");
                jLabel9.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Image/addItemImage.png")));
            }
            
        }
        catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTextField6KeyReleased

