    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //i +=1;
        
        String itemID1 = jTextField6.getText();
        String itemName1 = jTextField1.getText();
        String itemPrice1 = jTextField2.getText();
        String itemCategory1 = jTextField4.getText();
        String itemQuantity1 = (String)jComboBox1.getSelectedItem();
        String TotalPrice = jTextField5.getText();
        
        if(itemID1.isEmpty()){ 
            JOptionPane.showMessageDialog(rootPane, "Please Enter item ID","error",JOptionPane.ERROR_MESSAGE);
        }
        else{
        al = new ArrayList<String>();
        al.add(itemID1);
        al.add(itemName1);
        al.add(itemPrice1);
        al.add(itemCategory1);
        al.add(itemQuantity1);
        al.add(TotalPrice);
        
        hm.put(itemID1, al);
        
        JOptionPane.showMessageDialog(rootPane, "Successfully added");
        
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jComboBox1.setSelectedItem("Select Quantity");
        jTextField5.setText("");
        jTextField6.setText("");
        jLabel9.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Image/addItemImage.png")));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

